package ru.serdechniy.lab5;

public class Vektor {

    public double x0, x1, y0, y1, z0, z1, t0, t1;

    public Vektor(double x0, double x1, double y0, double y1, double z0, double z1, double t0, double t1) {
        this.x0 = x0;
        this.x1 = x1;
        this.y0 = y0;
        this.y1 = y1;
        this.z0 = z0;
        this.z1 = z1;
        this.t0 = t0;
        this.t1 = t1;
    }
}
